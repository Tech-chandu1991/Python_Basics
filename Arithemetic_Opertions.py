# Arithemetic operations in python
# Integers
first_num = 10
sec_num = 15

print("Enter the result Sum: ", first_num + sec_num)
print("Enter the result Sub: ", first_num - sec_num)
print("Enter the result Mul: ", first_num * sec_num)
print("Enter the result Div: ", first_num / sec_num)
print("Enter the result Mod Div: ", first_num % sec_num)
print("Enter the result Exp: ", first_num ** sec_num)
print("Enter the result Floor Div: ", first_num // sec_num)

