# Logical Operations in python
first_num = 100
sec_num = 50

result = first_num > sec_num
print(" Result :",result)
result = first_num < sec_num
print(" Result :",result)
result = first_num == sec_num
print(" Result :",result)
result = first_num != sec_num
print(" Result :",result)
result = first_num >= sec_num
print(" Result :",result)
result = first_num <= sec_num
print(" Result :",result)
